function splitSentence(string) {
  let arrayString = [];
  let word = '';

  for (let i in string) {
     if ( string[i] === ' ') {
       arrayString.push(word);
       word = '';
     }
     word += string[i];
  }
  arrayString.push(word);
  return arrayString;
}
